// Passo 1: Ler o primeiro número
let numero1 = parseFloat(prompt("Digite o primeiro número:"));

// Passo 2: Ler o segundo número
let numero2 = parseFloat(prompt("Digite o segundo número:"));

// Passo 3: Ler o terceiro número
let numero3 = parseFloat(prompt("Digite o terceiro número:"));

// Passo 4: Calcular a soma dos três números
let soma = numero1 + numero2 + numero3;

// Passo 5: Calcular a média
let media = soma / 3;

// Passo 6: Exibir a média
console.log("A média dos números é: " + media);
